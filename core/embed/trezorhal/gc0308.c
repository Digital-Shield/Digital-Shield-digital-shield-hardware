int gc0308_select_page0(void) {
  uint8_t v = 0x00;
  return gc0308_write(0xfe, &v, 1);
}

int gc0308_read_id(int *id) {
  uint8_t v;
  int ret = 0;
  i2c4_init();
  int count = 10;
  while (count--) {
    ret = gc0308_select_page0();
    if ( ret != 0 )
    {
        printf("%d: select page 0 failed\n", count);
        continue;
    }
    ret = gc0308_read(0x00, &v, 1);
    if ( ret != 0 )
    {
        printf("%d: read id failed\n", count);
        continue;
    }
    printf("id = 0x%02x\n", v);
    *id = v;
    break;
  }
  return ret;
}
